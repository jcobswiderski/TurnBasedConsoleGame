#include "SpecialPower.h"

/**
 * Setting special power by default values.
 */
SpecialPower::SpecialPower() {
    this->name = "Hand attack";
    this->uses = 1;
    this->singleSpecialAttack = 0;
    this->strengthToAppend = 0;
    this->dexterityToAppend = 0;
    this->healthToAppend = 0;
};